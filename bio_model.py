def update_water_quality(TAN_prev: float, NO3_prev: float, CO2_prev, feed_kg: float, Q_water: float) -> tuple:
    """
    
    """
    total_feed = feed_kg * N_fish   #scaling up for the whole tank (?)
   
    TAN_pool = ((total_feed * PC * 0.094) * 1000 ) / V_water + max(0.0, TAN_prev) 
    
    # Removal Capacity
    # Added max(0.0, TAN_pool) to prevent negative float errors in scalar power
    r_N_capacity = ((n_BF * (max(0.0, TAN_pool) ** k_BF)) * V_BF) / V_water
    r_N_actual = min(r_N_capacity, TAN_pool)
    
    TAN_bio = max(TAN_pool - r_N_actual, 0.0)
    
    # Update Nitrate
    NO3_bio = max(0.0, NO3_prev) + r_N_actual
    
    # Now wwe add CO2 production
    # fish produce approx 400g co2 per kg of feed
    CO2_prod = (total_feed * 400) / V_water 

    #CO2_bio  = CO2_prev + CO2_prod - (k_strip * (CO2_prev - 0.5))
    #CO2_bio  = max(CO2_bio, 0.5)
    C_eq = 0.5 # Equilibrium CO2
    dt = 1.0   # 1 day
    
    C_inf = C_eq + (CO2_prod / k_strip)
    CO2_bio = C_inf + (CO2_prev - C_inf) * math.exp(-k_strip * dt)

    # Dilution
    dilution_factor = min(Q_water/V_water, 1.0)

    TAN_next = TAN_bio * (1 - dilution_factor) + TAN_fresh * dilution_factor

    NO3_next = NO3_bio * (1 - dilution_factor) + NO3_fresh * dilution_factor
    CO2_next = CO2_bio * (1 - dilution_factor) + CO2_fresh * dilution_factor

    return TAN_next, NO3_next, CO2_next
   

