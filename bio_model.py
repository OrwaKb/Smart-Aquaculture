def run_sim(ind, initial_weight, initial_Tan, initial_Nitrate=0.0, initial_CO2 = 0.5):
    
    feeds      = [float(t[0]) for t in ind]    # from the individual generated by the GA, the first is the feed
    temps      = [float(t[1]) for t in ind]
    DOs        = [float(t[2]) for t in ind]
    Q_waters   = [float(t[3]) for t in ind]

    t_span = (0.0, 1.0)  # integrate over 1 day

    weights  = [initial_weight]
    tan_lst  = [initial_Tan]
    no3_lst  = [initial_Nitrate]
    co2_lst  = [initial_CO2]
    feeds_kg = []
    
    for day in range(len(ind)):

        w_current      = weights[-1]
        tan_tmp        = tan_lst[-1]
        no3_tmp        = no3_lst[-1]
        co2_tmp        = co2_lst[-1]

        UIA_current = ammonia(tan_tmp)

        current_feed = feeds[day]
        current_temp = temps[day]
        current_DO   = DOs[day]
        current_Q    = Q_waters[day]

        sol = solve_ivp(
            lambda t, w: dwdt(t, w, UIA_current, current_feed, current_temp, current_DO),
            t_span,
            [w_current],
            t_eval=[t_span[1]]
        )

        if not sol.success:
            raise RuntimeError(f"ODE failed on day {day}: {sol.message}")

        w_tmp = sol.y[0, -1]
        weights.append(w_tmp)

        # Update TAN, feed and nitrate
        feed_kg = current_feed * w_current
        tan_tmp, no3_tmp, co2_tmp= update_water_quality(tan_tmp, no3_tmp,
                                                         co2_tmp, feed_kg, 
                                                         current_Q)
        
        tan_lst.append(tan_tmp)
        no3_lst.append(no3_tmp)
        co2_lst.append(co2_tmp)
        feeds_kg.append(feed_kg)

    return weights, feeds_kg, tan_lst, no3_lst, co2_lst, temps, DOs, Q_waters


def profit(w_final: float,
           w_initial: float,
           f_list,
           T_list,
           DO_list,
           Q_list,
           days) -> float:
    """
    Bio-economic profit for a given period.

    :param w_final:  final fish weight (kg)
    :param w_initial: initial fish weight (kg)
    :param f_list:   list of feed amounts in kg over the period
    :param T:        water temperature (°C)
    :param DO:       dissolved oxygen (mg/L)
    :param days:     number of days in this profit period
    """

    assert len(f_list) == len(T_list) == len(DO_list) == days

    # ================= Revenue =================
    revenue = (w_final - w_initial) * p_fish * N_fish

    # ================= Feed cost =================
    total_feed = 0
    
    for f in f_list:
        total_feed += f

    feed_cost = total_feed * p_feed * N_fish

   # ================= Electricity costs =================

    heat_cost = 0.0
    DO_cost   = 0.0
    water_cost= 0.0

    for T, DO, Q in zip(T_list, DO_list, Q_list):
        T  = float(np.asarray(T).squeeze())
        DO = float(np.asarray(DO).squeeze())
        Q  = float(np.asarray(Q).squeeze())

        heat_flux_surf = area * 12.12 * max(0.0, T - T_env) #/ N_fish

        heat_flux_ex   = (Q * 1000.0 * Cp_water * (T - T_fresh))/ (24 * 3600)

        total_heat = heat_flux_surf + (heat_flux_ex) #/ N_fish)
        heat_cost += (total_heat / 1000.0) * electricity_price * 24.0

        water_cost += (Q * p_water) #/ N_fish

        DO_extra = max(0.0, DO - DO_base)
        DO_cost_per_m3 = ((DO_extra / 1000.0) / 0.46) * electricity_price
        DO_cost += DO_cost_per_m3 * (V_water) #/ N_fish)

    # ================= Total cost =================

    total_cost = feed_cost + heat_cost + DO_cost + water_cost
    
    return revenue - total_cost

