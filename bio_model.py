def ammonia(TAN: float) -> float:
    """
    Ammonia caclulation from page 42
    """
    pKa = PK_a # for now its a const
    pH = PH_cons # for now
    NH3_N = TAN / (1 + 10 ** (pKa - pH))
    
    return NH3_N


